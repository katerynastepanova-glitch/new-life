// Слова-зв'язки: початок нової задачі. Прибираються з початку фрагмента.
const CONNECTORS = new Set([
  "потім", "далі", "ще", "також", "наступне", "наступний",
  "і", "та", "а", "й",
]);

// Допоміжні слова перед дієсловом — прибираємо з початку задачі.
const LEADING_FILLER = new Set([
  "треба", "потрібно", "мені", "ще", "потім", "далі", "також",
  "і", "та", "а", "й", "то",
]);

function clean(word: string): string {
  return word.toLowerCase().replace(/[.,;!?…«»"'()]/g, "");
}

// Розбиваємо суцільний рядок: нова задача починається з дієслова-тригера
// (якщо вже є накопичені слова) або після слова-зв'язки.
function splitBlob(blob: string): string[] {
  const words = blob.trim().split(/\s+/).filter(Boolean);
  if (!words.length) return [];

  const tasks: string[] = [];
  let current: string[] = [];

  for (let i = 0; i < words.length; i++) {
    const w = clean(words[i]);
    const isVerb = TASK_VERBS.has(w);
    const isConnector = CONNECTORS.has(w);

    // Дієслово-тригер усередині фрази → починаємо нову задачу.
    if (isVerb && current.length > 0) {
      tasks.push(current.join(" "));
      current = [words[i]];
      continue;
    }

    // Слово-зв'язка перед дієсловом → межа задач.
    if (
      isConnector &&
      current.length > 0 &&
      i + 1 < words.length &&
      TASK_VERBS.has(clean(words[i + 1]))
    ) {
      tasks.push(current.join(" "));
      current = [];
      continue; // саму зв'язку відкидаємо
    }

    current.push(words[i]);
  }

  if (current.length) tasks.push(current.join(" "));
  return tasks;
}

// Прибираємо допоміжні слова з початку задачі ("треба купити" → "купити").
function trimLeading(task: string): string {
  const words = task.trim().split(/\s+/);
  let start = 0;
  while (start < words.length - 1 && LEADING_FILLER.has(clean(words[start]))) {
    start++;
  }
  return words.slice(start).join(" ");
}

// Чи складається фрагмент лише з допоміжних слів (тоді це не задача).
function isAllFiller(task: string): boolean {
  return task
    .trim()
    .split(/\s+/)
    .every((w) => LEADING_FILLER.has(clean(w)) || CONNECTORS.has(clean(w)));
}

export function parseTasks(input: string): string[] {
  // 1) Спершу ділимо за явними роздільниками: новий рядок, кома, крапка з комою,
  //    крапка, маркери списку.
  const chunks = input
    .split(/[\n;.•\-–—]+|,/)
    .map((c) => c.trim())
    .filter(Boolean);

  // 2) Кожен фрагмент додатково ділимо за дієсловами (для голосових блоків).
  const tasks: string[] = [];
  for (const chunk of chunks) {
    for (const t of splitBlob(chunk)) {
      if (isAllFiller(t)) continue; // відкидаємо "треба", "потім" тощо
      const cleaned = trimLeading(t).trim();
      if (cleaned) tasks.push(cleaned[0].toUpperCase() + cleaned.slice(1));
    }
  }

  return tasks;
}
